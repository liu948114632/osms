package com.itecheasy.core.fba;

import java.util.List;

/**
 * @author wanghw
 * @date 2016-12-1
 * @description C3补货商品查询条件
 * @version 1.2.2
 */
public class SearchFbaRelatedCMSProduct {
	private int currentPage;
	private int pageSize;
	private String cmsProductCode;
	private List<String> cmsProductCodes;
	/**
	 * 查询的是达到提醒c3补货的数据（即待补货批量>0） 查询的是没达到提醒c3补货条件的数据（即待补货批量=0）
	 */
	private int isRemindCMSReplenishment;

	public int getCurrentPage() {
		return currentPage;
	}

	public void setCurrentPage(int currentPage) {
		this.currentPage = currentPage;
	}

	public int getPageSize() {
		return pageSize;
	}

	public void setPageSize(int pageSize) {
		this.pageSize = pageSize;
	}

	public String getCmsProductCode() {
		return cmsProductCode;
	}

	public void setCmsProductCode(String cmsProductCode) {
		this.cmsProductCode = cmsProductCode;
	}

	public List<String> getCmsProductCodes() {
		return cmsProductCodes;
	}

	public void setCmsProductCodes(List<String> cmsProductCodes) {
		this.cmsProductCodes = cmsProductCodes;
	}

	public int getIsRemindCMSReplenishment() {
		return isRemindCMSReplenishment;
	}

	public void setIsRemindCMSReplenishment(int isRemindCMSReplenishment) {
		this.isRemindCMSReplenishment = isRemindCMSReplenishment;
	}

}
