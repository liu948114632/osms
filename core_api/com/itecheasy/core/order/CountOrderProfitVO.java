package com.itecheasy.core.order;

import java.math.BigDecimal;
import java.util.List;

/**
 * @author wanghw
 * @date 2016-1-14
 * @description 月度毛利统计返回结果
 * @version
 */
public class CountOrderProfitVO {
	private List<OrderProfitReport> orderProfitReports;
	private List<OrderProfitReportMessage> messages;
	private List<OrderCountReport> countReports;
	private BigDecimal profitPercent;

	public List<OrderCountReport> getCountReports() {
		return countReports;
	}

	public void setCountReports(List<OrderCountReport> countReports) {
		this.countReports = countReports;
	}


	public BigDecimal getProfitPercent() {
		return profitPercent;
	}

	public void setProfitPercent(BigDecimal profitPercent) {
		this.profitPercent = profitPercent;
	}

	public List<OrderProfitReportMessage> getMessages() {
		return messages;
	}

	public void setMessages(List<OrderProfitReportMessage> messages) {
		this.messages = messages;
	}

	public List<OrderProfitReport> getOrderProfitReports() {
		return orderProfitReports;
	}

	public void setOrderProfitReports(List<OrderProfitReport> orderProfitReports) {
		this.orderProfitReports = orderProfitReports;
	}

}
