package com.itecheasy.core.operation;

/**
 * @author taozihao
 * @date 2018年8月24日 下午6:53:39
 * @description	降价商品sku以及对应的降价百分比
 */
public class PriceOffPercentVO {
	
	private int clearInventoryTrackingId;
	
	private String sku;
	/**
	 * 降价百分比
	 * 需要除以100
	 */
	private int priceOffPercent;

	public int getClearInventoryTrackingId() {
		return clearInventoryTrackingId;
	}

	public void setClearInventoryTrackingId(int clearInventoryTrackingId) {
		this.clearInventoryTrackingId = clearInventoryTrackingId;
	}

	public String getSku() {
		return sku;
	}

	public void setSku(String sku) {
		this.sku = sku;
	}

	public int getPriceOffPercent() {
		return priceOffPercent;
	}

	public void setPriceOffPercent(int priceOffPercent) {
		this.priceOffPercent = priceOffPercent;
	}
}
