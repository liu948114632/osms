package com.itecheasy.core.operation;

import java.math.BigDecimal;
import java.util.Date;

/**
 * @author taozihao
 * @date 2018年8月24日 下午6:13:45
 * @description
 */
public class ClearInventoryTrackingRecordVO {
	/**
	 * 降价时间
	 */
	private Date priceOffDate;
	/**
	 * 第几周
	 */
	private int weekOfYear;
	/**
	 * 售价
	 */
	private BigDecimal salePrice;
	/**
	 * 备注
	 */
	private String remark;

	public Date getPriceOffDate() {
		return priceOffDate;
	}

	public void setPriceOffDate(Date priceOffDate) {
		this.priceOffDate = priceOffDate;
	}

	public int getWeekOfYear() {
		return weekOfYear;
	}

	public void setWeekOfYear(int weekOfYear) {
		this.weekOfYear = weekOfYear;
	}

	public BigDecimal getSalePrice() {
		return salePrice;
	}

	public void setSalePrice(BigDecimal salePrice) {
		this.salePrice = salePrice;
	}

	public String getRemark() {
		return remark;
	}

	public void setRemark(String remark) {
		this.remark = remark;
	}

}
